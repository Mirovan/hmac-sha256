package com.example.demo.model;

public class IndividualData {
    private String lastName;
    private String firstName;
    private String patronymic;
    private String birthdate;
    private String passportSeries;
    private String passportNumber;
    private String driverLicenceSeries;
    private String driverLicenceNumber;
    private String driverLicenceIssueDate;
    private String inn;

    public IndividualData() {
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getPatronymic() {
        return patronymic;
    }

    public void setPatronymic(String patronymic) {
        this.patronymic = patronymic;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getPassportSeries() {
        return passportSeries;
    }

    public void setPassportSeries(String passportSeries) {
        this.passportSeries = passportSeries;
    }

    public String getPassportNumber() {
        return passportNumber;
    }

    public void setPassportNumber(String passportNumber) {
        this.passportNumber = passportNumber;
    }

    public String getDriverLicenceSeries() {
        return driverLicenceSeries;
    }

    public void setDriverLicenceSeries(String driverLicenceSeries) {
        this.driverLicenceSeries = driverLicenceSeries;
    }

    public String getDriverLicenceNumber() {
        return driverLicenceNumber;
    }

    public void setDriverLicenceNumber(String driverLicenceNumber) {
        this.driverLicenceNumber = driverLicenceNumber;
    }

    public String getDriverLicenceIssueDate() {
        return driverLicenceIssueDate;
    }

    public void setDriverLicenceIssueDate(String driverLicenceIssueDate) {
        this.driverLicenceIssueDate = driverLicenceIssueDate;
    }

    public String getInn() {
        return inn;
    }

    public void setInn(String inn) {
        this.inn = inn;
    }
}
